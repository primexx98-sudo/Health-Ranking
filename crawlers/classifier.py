def classify(product_name: str, brand: str = "") -> str:
    text = (product_name + " " + brand).lower()
    for category, keywords in CATEGORY_MAP:
        if not keywords:
            return category
        for kw in keywords:
            if kw.lower() in text:
                return category
    return "기타/특수목적"
